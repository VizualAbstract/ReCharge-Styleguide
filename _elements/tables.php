<p>Tables are a special beast. The problem with many responsive solutions is that you loose context of data comparison, which defeats the average purpose of tables.</p>
<hr>
<p>Meanwhile, previous approaches of allowing a user to scroll across data columns also have the problem losing the context of data focus, with the first column often representing a title for the data row.</p>
